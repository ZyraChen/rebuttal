import google.generativeai as genai
import json
import time
from typing import Dict, List, Optional
from tqdm import tqdm
import logging
import os
from datetime import datetime
from pathlib import Path
# 设置日志
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)


class ReferenceBasedEvaluator:
    """基于参考解释的LLM评估器"""
    
    def __init__(self, api_key: str, model: str = "gemini-3-flash-preview"):
        """
        初始化评估器
        
        Args:
            api_key: Google API key
            model: 模型名称，推荐使用 gemini-1.5-flash (免费额度更多)
        """
        genai.configure(api_key=api_key,
                        transport='rest',
                        client_options={'api_endpoint': 'https://xh.v1api.cc'}
                        )
        self.model = genai.GenerativeModel(model)
        self.model_name = model
        
        # 速率限制设置
        self.min_request_interval = 4  # 每次请求间隔4秒 (适用于 15 RPM 限制)
        self.last_request_time = 0
        
        logger.info(f"初始化评估器: {model}")
    
    def _rate_limit(self):
        """速率限制，防止超出API配额"""
        current_time = time.time()
        time_since_last = current_time - self.last_request_time
        
        if time_since_last < self.min_request_interval:
            wait_time = self.min_request_interval - time_since_last
            logger.debug(f"速率限制: 等待 {wait_time:.1f} 秒")
            time.sleep(wait_time)
        
        self.last_request_time = time.time()
    
    def _call_llm_with_retry(self, prompt: str, max_retries: int = 3) -> str:
        """
        带重试机制的LLM调用
        
        Args:
            prompt: 提示词
            max_retries: 最大重试次数
            
        Returns:
            LLM响应文本
        """
        for attempt in range(max_retries):
            try:
                self._rate_limit()
                
                response = self.model.generate_content(
                    prompt,
                    generation_config=genai.types.GenerationConfig(
                        temperature=0
                    )
                )
                
                return response.text
                
            except Exception as e:
                error_str = str(e)
                
                # Processing配额限制error
                if "429" in error_str or "quota" in error_str.lower() or "resource" in error_str.lower():
                    wait_time = 60 * (attempt + 1)
                    logger.warning(f"配额限制，等待 {wait_time} 秒后重试...")
                    time.sleep(wait_time)
                
                # Processing其他error
                else:
                    logger.error(f"API调用failed (尝试 {attempt+1}/{max_retries}): {error_str[:200]}")
                    if attempt < max_retries - 1:
                        time.sleep(5)
                    else:
                        raise Exception(f"LLM调用failed: {error_str}")
        
        raise Exception("达到最大重试次数")
    
    def evaluate_single(
        self, 
        claim: str,
        ground_truth: str,
        generated_explanation: str,
        reference_explanation: str,
        verdict: str
    ) -> Dict:
        """
        评估单个生成解释
        
        Args:
            claim: 待核查声明
            evidence: 检索到的证据
            generated_explanation: 生成的解释
            reference_explanation: 参考解释（人工标注）
            verdict: 判断结果
            
        Returns:
            评估结果字典
        """
        prompt = f"""The current date is January 12, 2026. You need to evaluate the quality of an explanation generated by a fact-checking system.

        【Claim to Verify】
        {claim}

        【Reference Explanation】 (human-annotated high-quality explanation)
        {reference_explanation}

        【Generated Explanation】 (to be evaluated)
        {generated_explanation}

        【Ground Truth】
        {ground_truth}

        【Generated Verdict】
        {verdict}

        Please rate the following dimensions on a scale of 1-5:

        1. **Correctness**: Whether the core arguments and conclusions in the generated explanation are accurate
           - 5: Core arguments are completely correct and consistent with the reference explanation
           - 4: Core arguments are mostly correct with minor discrepancies
           - 3: Core arguments are partially correct but with noticeable deviations
           - 2: Core arguments contain significant errors
           - 1: Core arguments are completely incorrect

        2. **Completeness**: Whether the generated explanation covers the key reasoning points from the reference explanation
           - 5: Covers all key reasoning points
           - 4: Covers most key reasoning points with minor omissions
           - 3: Covers main reasoning points but misses some important content
           - 2: Omits multiple important reasoning points
           - 1: Covers almost none of the key reasoning points

        3. **Faithfulness**: Whether the generated explanation is faithful to the provided evidence
           - 5: Completely faithful to evidence with no fabricated content
           - 4: Mostly faithful to evidence with minimal inaccuracies
           - 3: Generally faithful to evidence but with some inaccurate descriptions
           - 2: Contains substantial content not supported by evidence
           - 1: Severely distorts or fabricates evidence content

        4. **Coherence**: Whether the logic in the generated explanation is clear and coherent
           - 5: Logic is very clear with smooth reasoning flow
           - 4: Logic is mostly clear with slight incoherence
           - 3: Logic is generally understandable but somewhat disorganized
           - 2: Logic is confused and reasoning process is difficult to follow
           - 1: Completely incoherent

        5. **Clarity**: Whether the generated explanation presents information in a clear and well-structured manner
           - 5: Exceptionally clear with excellent structure and organization
           - 4: Clear and well-organized with minor structural issues
           - 3: Adequately clear but organization could be improved
           - 2: Somewhat unclear with poor organization that hinders understanding
           - 1: Very unclear and poorly structured, making comprehension difficult

        Please analyze:
        - What key points from the reference explanation are missing in the generated explanation?
        - Does the generated explanation contain valuable additional information not present in the reference?
        - What are the obvious errors in the generated explanation?

        Return in JSON format (do not wrap with markdown code blocks):
        {{
          "correctness": <score>,
          "completeness": <score>,
          "faithfulness": <score>,
          "coherence": <score>,
          "clarity": <score>,
          "overall": <average of the five dimensions>,
          "missing_key_points": ["missing key point 1", "missing key point 2"],
          "additional_valuable_points": ["additional valuable information 1"],
          "errors": ["error 1", "error 2"],
          "brief_reasoning": "Brief evaluation in 2-3 sentences"
        }}
        """
        
        try:
            response = self._call_llm_with_retry(prompt)
            
            # 解析JSON响应
            # 去除可能的markdown代码块标记
            response = response.strip()
            if response.startswith("```json"):
                response = response[7:]
            if response.startswith("```"):
                response = response[3:]
            if response.endswith("```"):
                response = response[:-3]
            response = response.strip()
            
            result = json.loads(response)
            
            # 计算overall（如果LLM没有计算）
            if "overall" not in result or result["overall"] == 0:
                scores = [
                    result.get("correctness", 0),
                    result.get("completeness", 0),
                    result.get("faithfulness", 0),
                    result.get("coherence", 0),
                    result.get("conciseness", 0)
                ]
                result["overall"] = sum(scores) / len(scores) if scores else 0
            
            return result
            
        except json.JSONDecodeError as e:
            logger.error(f"JSON解析failed: {str(e)}")
            logger.error(f"响应内容: {response[:500]}")
            
            # 返回默认分数
            return {
                "correctness": 3.0,
                "completeness": 3.0,
                "faithfulness": 3.0,
                "coherence": 3.0,
                "conciseness": 3.0,
                "overall": 3.0,
                "missing_key_points": [],
                "additional_valuable_points": [],
                "errors": [],
                "brief_reasoning": "JSON解析failed，使用默认分数",
                "parse_error": str(e)
            }

    def evaluate_batch(
            self,
            test_data: List[Dict],
            ref_data: List[Dict],
            save_path: str = "evaluation_results.json",
            batch_size: int = 50,
            resume: bool = True
    ) -> List[Dict]:
        """
        批量评估，支持断点续传

        Args:
            test_data: 测试数据列表
            ref_data: 参考数据列表（通过 claim 匹配）
            save_path: 结果保存路径
            batch_size: 每批Processing数量
            resume: 是否从上次中断处继续
        """
        # 1. 构建 ref_data 的索引字典，使用 claim 作为键
        ref_dict = {}
        for item in ref_data:
            claim = item.get('claim', '').strip()
            if claim:
                ref_dict[claim] = item

        logger.info(f"构建参考数据索引: {len(ref_dict)} 条")

        # 2. Loading已有结果（如果resume=True）
        results = []
        completed_ids = set()

        if resume and os.path.exists(save_path):
            with open(save_path, 'r', encoding='utf-8') as f:
                results = json.load(f)
                # 使用 claim 作为已completed标记
                completed_ids = {r.get('claim', '') for r in results if 'claim' in r}
                logger.info(f"Loading已有结果: {len(results)} 条")

        # 3. 过滤出未completed的数据
        remaining_data = [d for d in test_data
                          if d.get('claim', '').strip() not in completed_ids]

        logger.info(f"总数据: {len(test_data)}")
        logger.info(f"已completed: {len(completed_ids)}")
        logger.info(f"待Processing: {len(remaining_data)}")

        if not remaining_data:
            logger.info("✓ 所有数据已评估completed!")
            return results

        # 4. 分批Processing
        for batch_idx in range(0, len(remaining_data), batch_size):
            batch_end = min(batch_idx + batch_size, len(remaining_data))
            batch = remaining_data[batch_idx:batch_end]

            logger.info(f"\n{'=' * 70}")
            logger.info(f"Processing批次 {batch_idx // batch_size + 1}: "
                        f"[{batch_idx + 1}-{batch_end}] / {len(remaining_data)}")
            logger.info(f"{'=' * 70}")

            # Processing当前批次
            for local_idx, item in enumerate(tqdm(batch, desc=f"批次 {batch_idx // batch_size + 1}")):
                global_idx = batch_idx + local_idx + 1

                try:
                    # 5. 根据 claim 查找对应的参考数据
                    claim = item.get('claim', '').strip()

                    if not claim:
                        logger.error(f"  ✗ 测试数据缺少 claim 字段")
                        results.append({
                            'claim': '',
                            'error': "Missing claim in test data",
                            'timestamp': datetime.now().isoformat()
                        })
                        continue

                    if claim not in ref_dict:
                        logger.error(f"  ✗ 找不到对应的参考数据: {claim[:50]}...")
                        results.append({
                            'claim': claim,
                            'error': f"Missing reference data for claim",
                            'timestamp': datetime.now().isoformat()
                        })
                        continue

                    ref = ref_dict[claim]

                    logger.info(f"\n[{global_idx}/{len(remaining_data)}] "
                                f"评估: {claim[:50]}...")

                    # 6. 获取证据（从 evidence_sources 中提取）
                    evidence_list = []
                    if 'evidence_sources' in item:
                        for src in item['evidence_sources']:
                            if 'content' in src:
                                evidence_list.append(src['content'])
                    evidence = '\n'.join(evidence_list) if evidence_list else item.get('evidence', '')

                    # 7. 执行评估
                    scores = self.evaluate_single(
                        claim=claim,
                        ground_truth=ref.get('ground_truth',''),
                        generated_explanation=item.get('justification', ''),
                        reference_explanation=ref.get('justification', ''),
                        verdict=item.get('verdict', {}).get('decision', '')
                    )

                    # 8. 构建结果
                    result = {
                        'claim': claim,
                        'verdict': item.get('verdict', ''),
                        'scores': scores,
                        'timestamp': datetime.now().isoformat()
                    }

                    results.append(result)

                    logger.info(f"  ✓ Overall: {scores['overall']:.2f}")
                    logger.info(f"  - Correctness: {scores['correctness']:.1f}")
                    logger.info(f"  - Completeness: {scores['completeness']:.1f}")
                    logger.info(f"  - Faithfulness: {scores['faithfulness']:.1f}")

                except Exception as e:
                    logger.error(f"  ✗ 评估failed: {str(e)[:200]}")
                    import traceback
                    logger.error(traceback.format_exc())

                    results.append({
                        'claim': item.get('claim', ''),
                        'error': str(e),
                        'timestamp': datetime.now().isoformat()
                    })

                # 9. 每10条保存一次
                if (local_idx + 1) % 10 == 0:
                    self._save_results(results, save_path)
                    logger.info(f"  💾 已保存中间结果")

            # 批次End后保存
            self._save_results(results, save_path)
            logger.info(f"💾 批次completed，已保存结果")

            # 批次间休息（避免配额限制）
            if batch_end < len(remaining_data):
                rest_time = 60
                logger.info(f"😴 批次间休息 {rest_time} 秒...\n")
                time.sleep(rest_time)

        logger.info(f"\n{'=' * 70}")
        logger.info(f"✓ 评估completed! 总计: {len(results)} 条")
        logger.info(f"{'=' * 70}")

        return results
    
    def _save_results(self, results: List[Dict], save_path: str):
        """保存结果到文件"""
        with open(save_path, 'w', encoding='utf-8') as f:
            json.dump(results, f, ensure_ascii=False, indent=2)
    
    def aggregate_results(self, results: List[Dict]) -> Dict:
        """
        聚合统计结果
        
        Args:
            results: 评估结果列表
            
        Returns:
            聚合统计字典
        """
        # 过滤掉有error的结果
        valid_results = [r for r in results if 'scores' in r and 'error' not in r]
        error_results = [r for r in results if 'error' in r]
        
        if not valid_results:
            return {
                'error': 'No valid results',
                'total': len(results),
                'errors': len(error_results)
            }
        
        # 提取所有维度的分数
        dimensions = ['correctness', 'completeness', 'faithfulness', 
                     'coherence', 'conciseness', 'overall']
        
        aggregated = {}
        
        for dim in dimensions:
            scores = [r['scores'][dim] for r in valid_results 
                     if dim in r['scores']]
            
            if scores:
                aggregated[dim] = {
                    'mean': sum(scores) / len(scores),
                    'min': min(scores),
                    'max': max(scores),
                    'count': len(scores)
                }
        
        # 统计缺失关键点的情况
        missing_points_count = sum(
            len(r['scores'].get('missing_key_points', [])) 
            for r in valid_results
        )
        
        # 统计error的情况
        errors_count = sum(
            len(r['scores'].get('errors', [])) 
            for r in valid_results
        )
        
        aggregated['statistics'] = {
            'total_evaluated': len(valid_results),
            'total_errors': len(error_results),
            'avg_missing_points': missing_points_count / len(valid_results),
            'avg_errors': errors_count / len(valid_results)
        }
        
        return aggregated
    
    def print_summary(self, results: List[Dict]):
        """打印评估摘要"""
        aggregated = self.aggregate_results(results)
        
        if 'error' in aggregated:
            print(f"\n❌ {aggregated['error']}")
            return
        
        print("\n" + "="*70)
        print("评估结果摘要")
        print("="*70)
        
        print(f"\n总计评估: {aggregated['statistics']['total_evaluated']} 条")
        print(f"评估failed: {aggregated['statistics']['total_errors']} 条")
        
        print(f"\n各维度平均分:")
        print(f"  Correctness (正确性):   {aggregated['correctness']['mean']:.2f}")
        print(f"  Completeness (完整性):  {aggregated['completeness']['mean']:.2f}")
        print(f"  Faithfulness (忠实性):  {aggregated['faithfulness']['mean']:.2f}")
        print(f"  Coherence (连贯性):     {aggregated['coherence']['mean']:.2f}")
        print(f"  Conciseness (简洁性):   {aggregated['conciseness']['mean']:.2f}")
        print(f"  Overall (总体):         {aggregated['overall']['mean']:.2f}")
        
        print(f"\n平均缺失关键点数: {aggregated['statistics']['avg_missing_points']:.2f}")
        print(f"平均error数: {aggregated['statistics']['avg_errors']:.2f}")
        
        print("="*70)

def load_argcheck_results(logs_dir: Path) -> List[Dict]:

    """

    Loading ArgCheck (output/logs) 的结果



    Args:

        logs_dir: logs 目录路径



    Returns:

        结果列表

    """

    results = []
    #Reading所有 log_XXX.json 文件
    log_files = sorted(logs_dir.glob("claim_0*_step3_verdict.json"))
    print(f"找到 {len(log_files)} 个日志文件")
    for log_file in log_files:
        try:
            with open(log_file, "r", encoding="utf-8") as f:
                data = json.load(f)

            # 提取需要的信息
            result = {
                "claim": data.get("claim", ""),
                "ground_truth": data.get("ground_truth", ""),
                "decision": data.get("verdict", {}).get("decision", ""),
                "justification": data.get("verdict", {}).get("reasoning", ""),
                "confidence": data.get("verdict", {}).get("confidence", 0.5)
            }
            results.append(result)
        except Exception as e:
            print(f"⚠ Reading文件failed {log_file}: {e}")

    return results

